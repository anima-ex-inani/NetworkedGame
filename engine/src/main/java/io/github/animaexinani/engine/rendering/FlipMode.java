package io.github.animaexinani.engine.rendering;

/**
 * Represents the flip state for rendering textures.
 */
public enum FlipMode {
    /**
     * Do not flip.
     */
    NONE,

    /**
     * Flip horizontally.
     */
    HORIZONTAL,

    /**
     * Flip vertically.
     */
    VERTICAL,

    /**
     * Flip horizontally and vertically.
     */
    BOTH
}
