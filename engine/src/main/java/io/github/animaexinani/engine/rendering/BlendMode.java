package io.github.animaexinani.engine.rendering;

/**
 * Represents the blend mode used for rendering.
 */
public enum BlendMode {
    /**
     * No blending.
     */
    NONE,

    /**
     * Alpha blending.
     */
    BLEND,

    /**
     * Additive blending.
     */
    ADD,

    /**
     * Color modulate.
     */
    MOD,

    /**
     * Color multiply.
     */
    MUL
}
